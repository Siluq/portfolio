function changeColor(event) {
  if (event.target.getAttribute('src') !== null) {
    var attr = event.target.getAttribute('src');
    var color = ['pink', 'blush', 'yellow', 'green', 'overgreen', 'tropicgreen', 'seablue', 'ice', 'wood', 'dark', 'overyellow', 'orange', 'red'];
    var src = attr.substr(0, 10) + color[getRandColor(0, 12)] + '.jpg';
    event.target.src = src;
    // console.log(src);
  }
}

function getRandColor(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}
